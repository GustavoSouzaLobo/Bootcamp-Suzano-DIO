produto_1 = 10
produto_2 = 20

soma = produto_1 + produto_2
subtracao = produto_1 - produto_2
divisao = produto_1 / produto_2
multiplicacao = produto_1 * produto_2

print("A soma dos numeros {0} e {1} é igual a {2}".format(produto_1, produto_2, soma))
print("A subtração dos numeros {0} e {1} é igual a {2}".format(produto_1, produto_2, subtracao))
print("A divisao dos numeros {0} e {1} é igual a {2}".format(produto_1, produto_2, divisao))
print("A multiplicação dos numeros {0} e {1} é igual a {2}".format(produto_1, produto_2, multiplicacao))